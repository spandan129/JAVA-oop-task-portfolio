/// Task 3. Program that outputs some personal details to the screen, e.g. Name, Address, Age, Phone number etc.
public class PersonalDetails {
    public static void main(String[] args) {
        String name = "Spandan Bhattarai";
        String address = "Sitapaila, Kathmandu.";
        int age = 18;
        long phoneNumber = 9849752143L; 

        System.out.println("Name: " + name);
        System.out.println("Address: " + address);
        System.out.println("Age: " + age);
        System.out.println("Phone Number: " + phoneNumber);
    }
}
